# 2. –ê—Å—É—É–ª—Ç –∞—Å—É—É—Ö —Ñ—É–Ω–∫—Ü
def ask_question(question, answer):
    user_answer = input(question + " ")
    if user_answer.lower() == answer.lower():
        print(" –ó”©–≤!")
        return 1  # –û–Ω–æ–æ 1 –Ω—ç–º–Ω—ç
    else:
        print(" –ë—É—Ä—É—É!")
        return 0  # –û–Ω–æ–æ –Ω—ç–º—ç—Ö–≥“Ø–π


